package data;

/**
 *
 * @author pc
 */
public class Positions {
    int IdPositions;
    String NamePositions;

    public Positions() {
    }

    public Positions(int IdPositions, String NamePositions) {
        this.IdPositions = IdPositions;
        this.NamePositions = NamePositions;
    }

    public int getIdPositions() {
        return IdPositions;
    }

    public void setIdPositions(int IdPositions) {
        this.IdPositions = IdPositions;
    }

    public String getNamePositions() {
        return NamePositions;
    }

    public void setNamePositions(String NamePositions) {
        this.NamePositions = NamePositions;
    }
    
    
}
